#bit-wise operators
print("___&___")
a=5              # 0101
b=3              # 0011
print(a & b)     # 1 (0001)

print("____|____")
print(5 | 3)# 7       #0101
                      #0011

print("___^___")
print(5^3)#6         #0101
                     #0011
                     #0110-6
print("_____~__")
print(~5)#-6           #-(n+1)

print("_____>>___")
print(5>>3) #0      #0000101
                    #0000000
print("____<<____")
print(5<<3)#40      #0000101
                     #0101000-40
                    